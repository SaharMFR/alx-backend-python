#!/usr/bin/env python3
""" Defines a type-annotated function `add` """


def add(a: float, b: float) -> float:
    """
    Adds two arguments.

    Parameters:
    a: the first number to add (float).
    b: The second number to add (float).

    Return:
    float: The sum of a and b.
    """
    return a + b
